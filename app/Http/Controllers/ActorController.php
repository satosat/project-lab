     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $actors = null;

        if ($request->get('q')) {
            $actor = $request->get('q');
            $actors = Actor::where('name', 'LIKE', "%$actor%")->get();
        } else {
            $actors = Actor::all();
        }

        return view('actors.index', [
            'actors' => $actors,
        ]);
    }

